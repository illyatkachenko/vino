#ifndef HARDWARE_PINS_H
#define HARDWARE_PINS_H

// Этот файл содержит все определения аппаратных пинов для проекта Main-esp32.

// --- Пины управления мотором ---
// (Ранее определены в motor_control.c)
#define DIR_PIN             16 // Пин направления шагового двигателя
#define STEP_PIN            17 // Пин шага шагового двигателя
#define ENABLE_PIN          4  // Пин включения драйвера шагового двигателя (активный LOW)

// --- Пины кнопок ---
// (Ранее определены в main.c)
#define BTN_FWD             32 // Кнопка "Вперед" (ручное управление мотором)
#define BTN_REV             33 // Кнопка "Назад" (ручное управление мотором)
#define BTN_RESET           18 // Кнопка сброса/перезагрузки ESP32
#define BTN_POWER           5  // Кнопка включения/выключения питания системы

// --- Пины датчиков и исполнительных устройств ---
// (Ранее определены в sensors.h)
#define TEMP_IN_ADC_PIN         34  // ADC пин для датчика температуры на входе (NTC)
#define TEMP_COOLER_ADC_PIN     35  // ADC пин для датчика температуры охладителя (NTC)
#define TEMP_OUT_ADC_PIN        36  // ADC пин для датчика температуры на выходе (NTC) - VP
#define FLOW_SENSOR_PIN         25  // Пин для датчика потока (должен поддерживать прерывания)
#define COMPRESSOR_PIN          26  // Пин для управления реле компрессора

// --- Пин управления питанием ---
// (Ранее определен в main.c)
#define MOSFET_POWER_PIN    2  // Пин для управления MOSFET ключом основного питания периферии

#endif // HARDWARE_PINS_H